const MODBUS_RTU_CODE_RE = /\bcode=(\w+)/

export function parseModbusRtuErrorCode(message: string): string | null {
  const m = MODBUS_RTU_CODE_RE.exec(message)
  return m ? m[1] : null
}

export function getModbusRtuErrorI18n(code: string, lang: string): string {
  return MODBUS_RTU_ERROR_I18N[code]?.[lang as 'zh' | 'en'] || code
}

export function getErrorMessage(detail: string): string {
  if (!detail) return t('http.requestFailed')
  // 支持带参数的错误码格式: ERR_CODE:param
  const colonIdx = detail.indexOf(':')
  const code = colonIdx > 0 ? detail.substring(0, colonIdx) : detail
  const param = colonIdx > 0 ? detail.substring(colonIdx + 1) : ''
  const i18nKey = ERROR_CODE_MAP[code]
  if (i18nKey) {
    return param ? t(i18nKey, { field: param }) : t(i18nKey)
  }
  if (detail.startsWith('ERR_')) {
    const directKey = `errorCodes.${code}`
    const directMsg = t(directKey)
    if (directMsg !== directKey) return directMsg
    return t('http.requestFailed')
  }
  return detail
}

export function extractError(e: any, fallback?: string): string {
  // 优先检查拦截器提取的 userMessage（后端统一 {code, message, data, error_code} 格式）
  if (e?.userMessage && typeof e.userMessage === 'string') {
    return e.userMessage
  }

  // 检查拦截器提取的 errorCode
  if (e?.errorCode && typeof e.errorCode === 'string' && e.errorCode.startsWith('ERR_')) {
    const mapped = getErrorMessage(e.errorCode)
    if (mapped !== e.errorCode) return mapped
  }

  // 检查后端统一格式 {code, message, data, error_code}
  const respData = e?.response?.data
  if (respData && typeof respData === 'object') {
    // 新格式：error_code 在顶层
    const errorCode = respData.error_code
    if (typeof errorCode === 'string' && errorCode.startsWith('ERR_')) {
      const mapped = getErrorMessage(errorCode)
      if (mapped !== errorCode) return mapped
    }
    // 新格式：message 在顶层
    const topMessage = respData.message
    if (typeof topMessage === 'string' && topMessage) {
      if (topMessage.startsWith('ERR_')) {
        const mapped = getErrorMessage(topMessage)
        if (mapped !== topMessage) return mapped
      } else {
        return topMessage
      }
    }
  }

  // 检查业务错误（HTTP 200 + code !== 0）
  const bizMessage = e?.data?.message || (e?.isBusinessError ? e?.message : '')
  if (bizMessage) {
    const mapped = getErrorMessage(bizMessage)
    if (mapped !== bizMessage) return mapped
    return fallback || mapped
  }

  const detail = e?.response?.data?.detail
  if (typeof detail === 'string') {
    if (!detail.startsWith('ERR_')) return detail
    const mapped = getErrorMessage(detail)
    if (mapped !== detail) return mapped
    return fallback || mapped
  }
  if (typeof detail === 'object' && detail !== null) {
    if (typeof detail.error_code === 'string' && detail.error_code.startsWith('ERR_')) {
      const translatedCode = getErrorMessage(detail.error_code)
      const errorList = Array.isArray(detail.errors) ? detail.errors : []
      const warningList = Array.isArray(detail.warnings) ? detail.warnings : []
      const parts: string[] = []
      if (translatedCode !== detail.error_code) parts.push(translatedCode)
      if (errorList.length > 0) parts.push(...errorList)
      if (warningList.length > 0) parts.push(...warningList)
      const result = parts.length > 0 ? parts.join('\n') : translatedCode
      return result !== detail.error_code ? result : (fallback || result)
    }
    const msg = getErrorMessage(detail.message || '')
    const hintStr = detail.hint ? `\n${detail.hint}` : ''
    return `${msg}${hintStr}`
  }
  const errMsg = e?.message || ''
  if (errMsg.startsWith('ERR_')) return getErrorMessage(errMsg)
  return fallback || t('http.requestFailed')
}
